    private void initTableModels() {
        String stockHeaders[] = {"Title", "Authors", "Subject", "Number", "Year", "Volume", "Availability"};
        String bannedUsersHeaders[] = {"User", "Rights Refund Date"};
        String loansHeaders[] = {"Book", "User", "Start Date", "Due Date"};
        String usersHeaders[] = {"Type", "Name", "ID", "Faculty", "Department", "Group", "Specialty"};

        itemTable.setColumnIdentifiers(stockHeaders);
        bannedUsers.setColumnIdentifiers(bannedUsersHeaders);
        loans.setColumnIdentifiers(loansHeaders);

        Stock.setModel(itemTable);
        Stock.getColumnModel().getColumn(6).setCellRenderer(new DefaultTableCellRenderer() {
            @Override
            public Component getTableCellRendererComponent(JTable table, Object value, boolean isSelected, boolean hasFocus, int row, int column) {
                Component cell = super.getTableCellRendererComponent(table, value, isSelected, hasFocus, row, column);
                if (value instanceof Boolean isInUse) {
                    if (isInUse) {
                        cell.setBackground(new Color(74, 127, 81));
                        cell.setForeground(new Color(0, 0, 0));
                        setText("Available");
                    } else {
                        cell.setBackground(new Color(194, 190, 119));
                        cell.setForeground(new Color(0, 0, 0));
                        setText("Not Available");
                    }
                    setHorizontalAlignment(CENTER);
                } else {
                    cell.setBackground(Color.WHITE);
                    cell.setForeground(Color.BLACK);
                }
                return cell;
            }
        });

        UsersTable.setModel(users);
        UsersTable.setDefaultRenderer(Object.class, new DefaultTableCellRenderer() {
            @Override
            public Component getTableCellRendererComponent(JTable table, Object value, boolean isSelected, boolean hasFocus, int row, int column) {
                Component cell = super.getTableCellRendererComponent(table, value, isSelected, hasFocus, row, column);
                String userType = (String) table.getValueAt(row, 0);

                if (bundle.getString("radioButton.student").equals(userType)) {
                    cell.setBackground(new Color(74, 127, 81));
                } else if (bundle.getString("radioButton.teacher").equals(userType)) {
                    cell.setBackground(new Color(194, 190, 119));
                } else {
                    cell.setBackground(Color.WHITE);
                }

                return cell;
            }
        });

        BannedUsersTable.setModel(bannedUsers);
        LoansTable.setModel(loans);
    }

