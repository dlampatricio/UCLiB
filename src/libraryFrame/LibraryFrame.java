    private void RequestExtensionLabelMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_RequestExtensionLabelMouseClicked

        if (getUserIdForExtension.getText().isEmpty() || getBookTitleForExtension.getText().isEmpty()) {
            JOptionPane.showMessageDialog(this, bundle.getString("joptionpane.fillfields"), bundle.getString("joptionpane.warning"), JOptionPane.WARNING_MESSAGE);
            return;
        }

        try {
            library.requestExtension(getUserIdForExtension.getText(), getBookTitleForExtension.getText());
        } catch (MaxRenewalsExceededException mree) {
            JOptionPane.showMessageDialog(this, bundle.getString("joptionpane.extensionsLimit"), "Error", JOptionPane.ERROR_MESSAGE);
        } catch (NonExistentLoanException nele) {
            JOptionPane.showMessageDialog(this, bundle.getString("joptionpane.noloan"), "Error", JOptionPane.ERROR_MESSAGE);
        }

        updateLoansTable();
        getUserIdForExtension.setText("");
        getBookTitleForExtension.setText("");
    }//GEN-LAST:event_RequestExtensionLabelMouseClicked

