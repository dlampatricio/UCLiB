    private void addBookActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_addBookActionPerformed

        if (getBookTitle.getText().isEmpty() || getBookAuthors.getText().isEmpty() || getBookSubject.getText().isEmpty() || getBookNumber.getText().isEmpty() || getBookYear.getText().isEmpty()) {
            JOptionPane.showMessageDialog(this, bundle.getString("joptionpane.fillfields"), bundle.getString("joptionpane.warning"), JOptionPane.WARNING_MESSAGE);
            return;
        }

        String authorsText = getBookAuthors.getText();
        String subject = getBookSubject.getText();
        List<String> authorsList = Arrays.asList(authorsText.split(", "));

        try {
            Item book = new Book(authorsList, getBookSubject.getText(), getBookTitle.getText(), Integer.parseInt(getBookNumber.getText()), Integer.parseInt(getBookYear.getText()));
            library.addItem(book);
            itemTable.addRow(new Object[]{book.getTitle(), authorsText, subject, book.getNumber(), book.getYear(), "", !book.isInUse()});
        } catch (NumberFormatException nfe) {
            JOptionPane.showMessageDialog(this, bundle.getString("joptionpane.bookformat"), bundle.getString("joptionpane.warning"), JOptionPane.WARNING_MESSAGE);
            return;
        }

        getBookTitle.setText("");
        getBookAuthors.setText("");
        getBookSubject.setText("");
        getBookNumber.setText("");
        getBookYear.setText("");
        AddBookWindow.setVisible(false);
    }//GEN-LAST:event_addBookActionPerformed

