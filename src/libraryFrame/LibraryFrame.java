    private void AddUserButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_AddUserButtonActionPerformed

        if (StudentRadioButton.isSelected()) {
            if (getUserName.getText().isEmpty() || getUserID.getText().isEmpty() || getUserFaculty.getText().isEmpty() || getStudentSpecialty.getText().isEmpty() || getStudentGroup.getText().isEmpty()) {
                JOptionPane.showMessageDialog(this, "Please fill in all fields.", "Warning", JOptionPane.WARNING_MESSAGE);
                return;
            }
            User user = new Student(getStudentSpecialty.getText(), getStudentGroup.getText(), StudentAssistanceCheckBox.isSelected(), getUserName.getText(), getUserID.getText(), getUserFaculty.getText());
            for (User user2 : library.getUsers()) {
                if (user2.getId().equalsIgnoreCase(user.getId())) {
                    JOptionPane.showMessageDialog(this, bundle.getString("joptionpane.existentUser"), "Error", JOptionPane.ERROR_MESSAGE);
                    return;
                }
            }
            library.getUsers().add(user);
        } else if (TeacherRadioButton.isSelected()) {
            if (getUserName.getText().isEmpty() || getUserID.getText().isEmpty() || getUserFaculty.getText().isEmpty() || getTeacherDepartment.getText().isEmpty()) {
                JOptionPane.showMessageDialog(this, bundle.getString("joptionpane.fillfields"), bundle.getString("joptionpane.warning"), JOptionPane.WARNING_MESSAGE);
                return;
            }
            User user = new Teacher(getTeacherDepartment.getText(), getUserName.getText(), getUserID.getText(), getUserFaculty.getText());
            for (User user2 : library.getUsers()) {
                if (user2.getId().equalsIgnoreCase(user.getId())) {
                    JOptionPane.showMessageDialog(this, bundle.getString("joptionpane.existentUser"), "Error", JOptionPane.ERROR_MESSAGE);
                    return;
                }
            }

            library.getUsers().add(user);
        } else {
            JOptionPane.showMessageDialog(this, bundle.getString("joptionpane.selectType"), bundle.getString("joption.warning"), JOptionPane.WARNING_MESSAGE);
            return;
        }

        updateUsersTable();

        UserButtonGroup.clearSelection();
        getUserName.setText("");
        getUserID.setText("");
        getUserFaculty.setText("");
        getStudentSpecialty.setText("");
        getStudentGroup.setText("");
        getTeacherDepartment.setText("");
        SpecialtyLabel.setVisible(false);
        getStudentSpecialty.setVisible(false);
        GroupLabel.setVisible(false);
        getStudentGroup.setVisible(false);
        StudentAssistanceCheckBox.setVisible(false);
        DepartmentLabel.setVisible(false);
        getTeacherDepartment.setVisible(false);
        StudentAssistanceCheckBox.setSelected(false);
        AddUserWindow.setVisible(false);
        getUserName.setEnabled(false);
        getUserID.setEnabled(false);
        getUserFaculty.setEnabled(false);

    }//GEN-LAST:event_AddUserButtonActionPerformed

