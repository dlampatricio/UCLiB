    private void SearchButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_SearchButtonActionPerformed

        List<Item> searchResults = new ArrayList<>();
        itemTable.setRowCount(0);

        List<Item> results = library.searchByTitle(SearchTextField.getText());
        searchResults.addAll(results);

        results = library.searchByAuthor(SearchTextField.getText());
        searchResults.addAll(results);

        results = library.searchBySubject(SearchTextField.getText());
        searchResults.addAll(results);

        for (Item item : searchResults) {
            if (item instanceof Book book) {
                itemTable.addRow(new Object[]{book.getTitle(), String.join(", ", book.getAuthors()), book.getSubject(), book.getNumber(), book.getYear(), "", !book.isInUse()});
            } else if (item instanceof Magazine magazine) {
                itemTable.addRow(new Object[]{magazine.getTitle(), "", "", magazine.getNumber(), magazine.getYear(), magazine.getVol(), !magazine.isInUse()});
            }
        }

        SearchTextField.setText("");
        CancelSearchButton.setVisible(false);
    }//GEN-LAST:event_SearchButtonActionPerformed

